using System;

namespace HandlingAssignment1
{
    public class MathOps
    {
        public int AddFour(int num)
        {
            return num + 4;
        }

        public int SubTen(int num)
        {
            return num - 10; 
        }

        public int MultiNine(int num)
        {
            return num * 9;
        }
    }
}
